package homework_14_10_17.homeworkAram;

public class EvenAndOdd {

    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println("number even-" + i);
            } else {
                System.out.println("number odd-" + i);
            }
        }
    }

}
