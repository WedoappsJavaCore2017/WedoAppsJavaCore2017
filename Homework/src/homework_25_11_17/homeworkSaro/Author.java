public class Author {
   String name;
   String surname;
   int age;
   String birthDate;
   int booksCount;
   String gender;
   public Author( String name, String surname, int age, String birthDate, int booksCount, String gender){
       this.name = name;
       this.surname = surname;
       this.age = age;
       this.birthDate = birthDate;
       this.booksCount = booksCount;
       this.gender = gender;
   }

}
