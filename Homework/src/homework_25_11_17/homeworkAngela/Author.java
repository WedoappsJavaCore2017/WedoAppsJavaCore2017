public class Author {

    String name;
    String surname;
    int age;
    String birthDay;
    int booksCount;
    String gender;

    public Author(String name, String surname, int age, String birthDay, int booksCount, String gender) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.birthDay = birthDay;
        this.booksCount = booksCount;
        this.gender = gender;
    }
}
